"""
Простой модуль для математических операций.
"""

def add_numbers(a: int | float, b: int | float) -> int | float:
    """
    Возвращает сумму двух чисел.

    Args:
        a: Первое число.
        b: Второе число.

    Returns:
        Сумма a и b.
    """
    return a + b


def multiply_numbers(a: int | float, b: int | float) -> int | float:
    """
    Возвращает произведение двух чисел.

    Args:
        a: Первое число.
        b: Второе число.

    Returns:
        Произведение a и b.
    """
    return a * b
